package hwfor3rdweek;

public class DefaultVariable {
    static int intVar;
    static float floatVar;
    static double doubleVar;
    static String strVar;
    static short shortVar;
    public static void main(String[] args) {
        System.out.println("Default value for Integer is " + intVar);
        System.out.println("Default value for float is " + floatVar);
        System.out.println("Default value for String is " + strVar);
        System.out.println("Default value for Short is " + shortVar);
        System.out.println("Default value for double is " + doubleVar);

    }
}
